        public static void bubbleSort(Double[] a,String[] months) {
         for (int i = 0; i < a.length; i++)
          for (int j = 1; j < a.length - i; j++)
           if (a[j-1] > a[j]) {
            swap(a, j-1, j);
            swapMonth(months, j-1,j);
           }

}

private static void swap(Double[] a, int i, int j) {
if (i == j)
return;

double temp = a[i];
a[i] = a[j];
a[j] = temp;
}
private static void swapMonth(String[] a, int i, int j) {
if (i == j)
return;

String temp = a[i];
a[i] = a[j];
a[j] = temp;
}


