    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i < 3; i++) {
            String input = JOptionPane.showInputDialog("Enter the rainfall amount");
            Double rainAmt = Double.parseDouble(input);
            if (input !=null ) {
                rainfallAmt.add(rainAmt);
            } 
        }
        
        String output = "Index\tRainfall Amount\n" ;
        Iterator<Double> mona = rainfallAmt.iterator();
        int index = 0;
        while (mona.hasNext()) {
            double amount = mona.next();
             output = output + index +"\t" + amount + "\n";
             index++;
        }
        jTextArea1.setText(output);
        

        
    }//GEN-LAST:event_jButton1ActionPerformed

