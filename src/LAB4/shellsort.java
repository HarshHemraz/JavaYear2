  public static void shellSort(double[] time,String[] name ) {
int n = time.length;
// Start with a large gap, then reduce
for (int gap = n / 2; gap > 0; gap /= 2) {
// Perform gapped insertion sort

for (int i = gap; i < n; i++) {
double temp = time[i];
String tempHorse = name[i];
int j = i;
while (j >= gap && time[j - gap] > temp) {

time[j] = time[j - gap];
name[i] = name[j-gap];

j -= gap;
}
time[j] = temp;
name[j] = tempHorse;
}
}
}
  
   
    

