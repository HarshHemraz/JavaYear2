    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String output = "Time Taken\n";
        
        for (int x=0; x<timetake.length;x++) {
            
            String input = JOptionPane.showInputDialog("Enter the time taken of horse");
            double inputtime = Double.parseDouble(input);
            timetake[x] = inputtime; 
            output += timetake[x] + "\n";
        }
        
        
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        
        for (int i=0; i<timetake.length;i++) {
            
            if (i < model.getRowCount()) {
                model.setValueAt(timetake[i], i, 1);
            } else {
            
            
            Object[] row = new Object[model.getColumnCount()];
            row[1] = timetake[i];
            
            model.addRow(row);
            }
        }
        
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
                                           
    // Sort both arrays
    shellSort(timetake, horselist);
    
    // Update the JTable with sorted data
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);  // Clear existing rows
    
    // Add sorted data back to table
    for (int x = 0; x < horselist.length; x++) {
        model.addRow(new Object[]{horselist[x], timetake[x]});
    }
    
    // Display in text areaF
    String output = "Sorted Horse List According to Time Taken \n";
    for (int x = 0; x < timetake.length; x++) {
        output += horselist[x] + ": " + timetake[x] + " mins\n";
    }
    jTextArea1.setText(output);

        
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

