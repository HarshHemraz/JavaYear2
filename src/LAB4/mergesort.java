public static void merge(double[] arr, int left, int
mid, int right) {
    
    

int n1 = mid - left + 1;
int n2 = right - mid;

// Temp arrays
double[] L = new double[n1];
double[] R = new double[n2];

// Copy data
for (int i = 0; i < n1; i++)
L[i] = arr[left + i];
for (int j = 0; j < n2; j++)
R[j] = arr[mid + 1 + j];

// Merge temp arrays
int i = 0, j = 0, k = left;


while (i < n1 && j < n2) {
if (L[i] <= R[j]) {
arr[k] = L[i];
i++;
} else {
arr[k] = R[j];
j++;
}
k++;
}

// Copy remaining elements
while (i < n1) {
arr[k] = L[i];
i++; k++;
}
while (j < n2) {
arr[k] = R[j];


j++; k++;
}
}

// Recursive sort
public static void mergeSort(double[] arr, int left,
int right) {

if (left < right) {
int mid = (left + right) / 2;
mergeSort(arr, left, mid);
mergeSort(arr, mid + 1, right);
merge(arr, left, mid, right);
}
}

